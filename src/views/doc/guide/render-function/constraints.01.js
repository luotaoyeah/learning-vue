import { h } from 'vue';

export default {
  render() {
    /*----------------------------------------------------------------------------------------------------
     * 同一个 vnode 对象不能多处使用，vnode 必须唯一，
     * 即使两个 vnode 的内容完全一样，也应该使用工厂方法返回一个新的 vnode 对象，
     *----------------------------------------------------------------------------------------------------*/
    const fn = () => h('li', {}, ['foo']);
    return h(`ul`, {}, [fn(), fn(), fn()]);
  },
};
