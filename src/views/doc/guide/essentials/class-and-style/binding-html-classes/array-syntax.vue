<template>
  <div>
    <section>
      <!------------------------------------------------------------------------------------------------------
          当 class 的值为一个 array 时，每一个元素表示一个 class name，
      ------------------------------------------------------------------------------------------------------->
      <p :class="['foo', bar]">foo bar</p>

      <!------------------------------------------------------------------------------------------------------
          可以使用条件表达式 toggle 某个 class name，
      ------------------------------------------------------------------------------------------------------->
      <p :class="['foo', false ? 'bar' : '']">foo</p>

      <!------------------------------------------------------------------------------------------------------
          可以在 array syntax 中使用 object syntax，
      ------------------------------------------------------------------------------------------------------->
      <p :class="['foo', { bar: false, baz: true }, class01]">foo baz x</p>
    </section>
  </div>
</template>

<script>
// https://v3.vuejs.org/guide/class-and-style.html#array-syntax

export default {
  data() {
    return {
      bar: 'bar',
      class01: {
        x: true,
        y: false,
      },
    };
  },
};
</script>
