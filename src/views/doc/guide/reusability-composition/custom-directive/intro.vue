<template>
  <input v-border-radius type="text" />
</template>

<script>
// https://v3.vuejs.org/guide/custom-directive.html#intro

/*----------------------------------------------------------------------------------------------------
 * directive 可以全局注册或者本地注册，
 *----------------------------------------------------------------------------------------------------*/
export default {
  directives: {
    /*----------------------------------------------------------------------------------------------------
     * 注册时的名称中不包含 v- 前缀
     *----------------------------------------------------------------------------------------------------*/
    'border-radius': {
      mounted(el) {
        el.style.borderRadius = '3px';
      },
    },
  },
};
</script>
