<template><div></div></template>

<script>
// https://v3.vuejs.org/guide/component-props.html#prop-validation

class Person {}

/*----------------------------------------------------------------------------------------------------
 * 可以对 props 的 type/required/default 进行限制，
 *----------------------------------------------------------------------------------------------------*/
export default {
  props: {
    /*----------------------------------------------------------------------------------------------------
     * type 的值可以是任意的构造函数，底层使用 instanceof 来验证，
     *----------------------------------------------------------------------------------------------------*/
    prop01: String,
    prop02: [String, Person],
    prop03: { type: [Boolean] },
    /*----------------------------------------------------------------------------------------------------
     * required 默认为 false，
     *----------------------------------------------------------------------------------------------------*/
    prop04: { required: false },
    prop05: { type: String, default: 'foo' },
    /*----------------------------------------------------------------------------------------------------
     * 如果类型是 object/array，则 default 必须用函数返回，
     *----------------------------------------------------------------------------------------------------*/
    prop06: {
      type: Object,
      default() {
        return { name: 'luotao' };
      },
    },
  },
};
</script>
